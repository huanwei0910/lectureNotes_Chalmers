/*
什么是前置声明？

    在C++里，前置声明就是告诉编译器“有这么一个名字（类、函数等）存在”，但具体定义稍后会提供。

    这样编译器在编译当前代码时知道这个名字的存在，不会报错。

为什么需要前置声明？

    有时两个类相互引用（互为好友friend，或者成员中有对方指针），编译器会因为不知道另一个类而报错。

    通过前置声明，可以先告诉编译器“类X存在”，等完整定义后再使用。

    这样避免头文件循环包含（头文件相互包含），防止编译错误。

    还能稍微提高编译速度，因为不必在此处展开完整类定义。

OpenFOAM中的用法

    OpenFOAM在Foam命名空间里大量用前置声明提前告诉编译器某个类或函数的名字。

    这样它的头文件结构清晰，编译速度也更快。

*/
// File: main.cpp
#include "MyClass.H"
#include "YourClass.H"

int main() {
    MyClass m;
    YourClass y;
    m.doSomething(&y);
    y.doSomethingElse(&m);
    return 0;
}
